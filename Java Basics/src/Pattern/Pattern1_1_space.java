package Pattern;

public class Pattern1_1_space {

	public static void main(String[] args) {

		
		
		
		
		for ( int  i = 5 ; i>=1 ; i--)
		{
			
			for (int j = i ; j>1 ; j--)
			{
				System.out.print(" ");
			}
			
			
			for(int k = i ; k <= 5 ; k++)
			{
				System.out.print(k);
			}
			
			System.out.println();
		}
	
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
