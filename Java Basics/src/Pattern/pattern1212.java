package Pattern;

public class pattern1212 {

	public static void main(String[] args) {

		int sp = 4;
		
		for (int i = 1 ; i <= 5 ; i++)
		{
			
			
			for ( sp = 0 ; sp >=1 ; sp--)
			{
				System.out.print(" ");
				
			}
			
			
			for(int j = 1 ; j <= i ; j++)
			{
				System.out.print("*");
			}
			
			System.out.println();
			sp--;
		}
		
		
		
		
		
		
	}
	
}


